    private void Btn_AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_AddActionPerformed
        if (lastOperator== 1){ // pressed again without enter
            double result= Double.parseDouble(txt_Screen.getText())+ lastNumber;
            if (isInt(result)){
                int resultInt= (int) result;
                txt_Screen.setText(String.valueOf(resultInt));
            }
            else
                txt_Screen.setText(String.valueOf(result));
            lastNumber= result;
            clearOnNextInput= true;
        } // end if
        else if (lastOperator==0){            
            lastNumber= Double.parseDouble(txt_Screen.getText());
            lastOperator= 1; // addition
            txt_Screen.setText("");
            
        }  
        

    }//GEN-LAST:event_Btn_AddActionPerformed

