    public void AddNumberToScreen(ActionEvent e){
        if (clearOnNextInput== true)
            txt_Screen.setText("");
        
        clearOnNextInput= false;
        
        int digitLimits= 5;
        if (txt_Screen.getText().length() >= digitLimits) 
            return; // aşağıdaki concat operasyonları gerçekleştirilmez
        
        if(e.getSource()== Btn_0)
           txt_Screen.setText(txt_Screen.getText().concat("0"));
        else if(e.getSource()== Btn_1)
           txt_Screen.setText(txt_Screen.getText().concat("1"));
        else if(e.getSource()== Btn_2)
           txt_Screen.setText(txt_Screen.getText().concat("2"));
        else if(e.getSource()== Btn_3)
           txt_Screen.setText(txt_Screen.getText().concat("3"));
        else if(e.getSource()== Btn_4)
           txt_Screen.setText(txt_Screen.getText().concat("4"));
        else if(e.getSource()== Btn_5)
           txt_Screen.setText(txt_Screen.getText().concat("5"));
        else if(e.getSource()== Btn_6)
           txt_Screen.setText(txt_Screen.getText().concat("6"));
        else if(e.getSource()== Btn_7)
           txt_Screen.setText(txt_Screen.getText().concat("7"));
        else if(e.getSource()== Btn_8)
           txt_Screen.setText(txt_Screen.getText().concat("8"));
        else if(e.getSource()== Btn_9)
           txt_Screen.setText(txt_Screen.getText().concat("9"));
        else if(e.getSource()== Btn_Point){
           if (txt_Screen.getText().indexOf('.')== -1)
            txt_Screen.setText(txt_Screen.getText().concat("."));
        }
        
        
    }
    
